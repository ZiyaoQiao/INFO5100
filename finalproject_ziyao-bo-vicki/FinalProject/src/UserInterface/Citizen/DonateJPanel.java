    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        String name = txtItemName.getText();
        String num = txtQuantity.getText();
        if(name.equals("")||num.equals("")){
            JOptionPane.showMessageDialog(null, "The input cannot be blank!");
        }else{
                        
            boolean numInvalid = false;
            int quantity = -1;
            try{
                quantity = Integer.parseInt(num);
            }catch (Exception e){
                numInvalid = true;
                return;
            }
            
            if(quantity <1||numInvalid){
                JOptionPane.showMessageDialog(null,"wrong number!");
            }else{
                goods.setName(name);
                goods.setNum(quantity);
                goodsList.getGoodsList().add(goods);
                goods = null;
                txtItemName.setText("");
                txtQuantity.setText("");
                populateTable();
                btnAdd.setEnabled(false);
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

