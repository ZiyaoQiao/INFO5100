    private void SendDonationBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendDonationBtnActionPerformed
        // TODO add your handling code here:
        if(goodsList.getGoodsList().size()==0){
            JOptionPane.showMessageDialog(null, "no goods");
        }else{
            Enterprise ent = (Enterprise)CharityComboBox.getSelectedItem();
            SendWorkRequest workRequest = new SendWorkRequest();
            workRequest.setFromEnterprise(enterprise);
            workRequest.setToEnterprise(ent);
            workRequest.setRequest(1);
            workRequest.setRequestDate(new Date());
            workRequest.setSender(account);
            workRequest.setStatus(1);
            workRequest.setGoodsList(goodsList);
            workRequest.setFromOrganization(account.getOrganization());
            ent.getWorkQueue().getWorkRequestList().add(workRequest);
            account.getWorkQueue().getWorkRequestList().add(workRequest);

            goodsList = new GoodsList();
            JOptionPane.showMessageDialog(null, "Request Sent!");
            populateTable();
        }
    }//GEN-LAST:event_SendDonationBtnActionPerformed

