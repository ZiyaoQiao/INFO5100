    private void btnSendMessageActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendMessageActionPerformed
        String message = jTextArea1.getText();
        if(message.equals("")){
            JOptionPane.showMessageDialog(null,"please write");
        }else{
            Enterprise ent = null;
            for(Enterprise e: network.getEnterpriseDirectory().getEnterpriseList()){
                if(e instanceof GovEnterprise)
                    ent = e;
            }
            
            if(ent == null){
                JOptionPane.showMessageDialog(null, "No Gov Enterprise!");
            }else{
                Enterprise enter = (Enterprise)EnterpriseComboBox.getSelectedItem();
                ComplainWorkRequest workRequest = new ComplainWorkRequest();
                workRequest.setMessage(message);
                workRequest.setFromEnterprise(enterprise);
                workRequest.setRequestDate(new Date());
                workRequest.setSender(account);
                workRequest.setToEnterprise(ent);
                workRequest.setStatus(1);
                workRequest.setComplainEnterprise(enter);
                workRequest.setFromOrganization(account.getOrganization());
                
                for(Enterprise enterprise: network.getEnterpriseDirectory().getEnterpriseList()){
                    if(ent instanceof GovEnterprise){
                        ent.getWorkQueue().getWorkRequestList().add(workRequest);
                        break;
                    }
                }
                account.getWorkQueue().getWorkRequestList().add(workRequest);
                jTextArea1.setText("");
                JOptionPane.showMessageDialog(null, "Complain Sent!");
                populate();
            }
        }

 

    }//GEN-LAST:event_btnSendMessageActionPerformed

