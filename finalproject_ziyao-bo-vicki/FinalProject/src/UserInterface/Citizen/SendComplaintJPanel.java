    private void ViewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ViewBtnActionPerformed
        // TODO add your handling code here:
        int selected = HistoryTable.getSelectedRow();
        if(selected < 0){
            JOptionPane.showMessageDialog(null, "please select a row");
        }else{
            WorkRequest request = (WorkRequest)HistoryTable.getValueAt(selected, 0);
            if(request.getStatus() != 2){
                JOptionPane.showMessageDialog(null,"Not finished, no reply");
            }else{
                ReplyArea.setText(((ComplainWorkRequest)request).getResponse());
                ReplyArea.setEditable(false);
            }
        }
    }//GEN-LAST:event_ViewBtnActionPerformed


